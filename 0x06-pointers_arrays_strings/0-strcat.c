/**
 * _memset - copies data
 * @s: the source to copy it to
 * @b: the number of bytes to be overwritten
 * @n: integer variable
 *
 * Return: Nothing.
 */
char *_strcat(char *dest, char *src)
{
	unsigned int a;
	unsigned int b;
	unsigned int n;

	for (a = 0; a < n; a++)
		src[a] = b;
	return (dest);
}
